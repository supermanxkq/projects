package com.blog.dto.blogtype;

import java.io.Serializable;

import com.blog.bean.article.Article;
import com.blog.dto.BaseDTO;
public class BlogTypeDTO extends BaseDTO implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1773266644018159980L;
	private Integer blogTypeId;
	private String name;
	private Article article;

	public Article getArticle() {
		return article;
	}


	public String getName() {
		return name;
	}

	public void setArticle(Article article) {
		this.article = article;
	}


	public Integer getBlogTypeId() {
		return blogTypeId;
	}


	public void setBlogTypeId(Integer blogTypeId) {
		this.blogTypeId = blogTypeId;
	}


	public void setName(String name) {
		this.name = name;
	}

}
