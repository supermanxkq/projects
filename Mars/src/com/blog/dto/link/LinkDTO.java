package com.blog.dto.link;

import java.io.Serializable;

import com.blog.bean.links.LinkType;
import com.blog.dto.BaseDTO;

/**
 * @ProjectName:blog
 * @ClassName:link
 * @Description:超链接数据传输对象
 * @date: 2015-6-24下午04:44:15
 * @author: 徐半仙儿
 * @version: V1.0
 * @date:2015-6-24下午04:44:15
 */
public class LinkDTO extends BaseDTO implements Serializable {
	/** */
	private static final long serialVersionUID = -5760558050879346038L;
	/** 链接编号 */
	private Integer linkId;
	/** 链接名称 */
	private String linkName;
	/** 链接地址 */
	private String href;
	/** 链接类型 */
	private LinkType linkType;
	/**logo地址*/
	private String logoHref;
	public String getHref() {
		return href;
	}

	public Integer getLinkId() {
		return linkId;
	}

	public String getLinkName() {
		return linkName;
	}

	public LinkType getLinkType() {
		return linkType;
	}

	public String getLogoHref() {
		return logoHref;
	}

	public void setHref(String href) {
		this.href = href;
	}

	public void setLinkId(Integer linkId) {
		this.linkId = linkId;
	}

	public void setLinkName(String linkName) {
		this.linkName = linkName;
	}

	public void setLinkType(LinkType linkType) {
		this.linkType = linkType;
	}

	public void setLogoHref(String logoHref) {
		this.logoHref = logoHref;
	}
	
}
